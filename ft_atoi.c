#include "libft.h"

int	ft_atoi(char *str)
{
	int	minus;
	int	num;

	minus = 0;
	num = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			minus++;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		num = num * 10 + (*str - '0');
		str++;
	}
	if (minus % 2 == 1)
		num *= -1;
	return (num);
}
